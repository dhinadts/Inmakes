// 1.Write a Dart program to demonstrate the use of the increment operator (++).

void main() {
  int x = 5;
  print("The value x is $x");
  print('The value x is after post increment ${x++}');
  print('The value x is $x');
  print('The value x is after post increment ${++x}');
  print('The value x is $x');
}
